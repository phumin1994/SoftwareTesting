package bean;

public class StudentRegistration {
	
	private String firstName = null;
	private String lastName = null;
	private String personalIdentificationCode = null;
	private String gender = null;
	private String address = null;
	private String phonenumber = null;
	private String studyprogram = null;
	private String formofstudy = null;
	
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getPersonalIdentificationCode() {
		return personalIdentificationCode;
	}
	public void setPersonalIdentificationCode(String personalIdentificationCode) {
		this.personalIdentificationCode = personalIdentificationCode;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getPhonenumber() {
		return phonenumber;
	}
	public void setPhonenumber(String phonenumber) {
		this.phonenumber = phonenumber;
	}
	public String getStudyprogram() {
		return studyprogram;
	}
	public void setStudyprogram(String studyprogram) {
		this.studyprogram = studyprogram;
	}
	public String getFormofstudy() {
		return formofstudy;
	}
	public void setFormofstudy(String formofstudy) {
		this.formofstudy = formofstudy;
	}
}
